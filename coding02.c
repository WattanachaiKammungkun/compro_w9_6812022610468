#include <stdio.h>

int main() {
    // ----------------------------
    // โปรแกรมคูณ x * y และหาผลรวม
    // ----------------------------

    // ประกาศตัวแปร
    int sum, p, x, y;
    sum = 0;  // เริ่มต้นผลรวมที่ 0

    // วนลูปตั้งแต่ x = 1, y = 1 จนกว่า x * y > 15
    for (x = 1, y = 1; x * y <= 15; x++, y += 2) {
        p = x * y;         // คำนวณผลคูณของ x และ y
        sum = sum + p;     // รวมผลคูณลงในตัวแปร sum

        // แสดงค่าที่เปลี่ยนไปในแต่ละรอบ
        // เช่น รอบที่ 1: 1 * 1 = 1
        printf("%d * %d = %d\n", x, y, p);
    }

    // แสดงผลรวมทั้งหมดหลังจบลูป
    printf("Summation of x * y = %d\n", sum);

    return 0;
}